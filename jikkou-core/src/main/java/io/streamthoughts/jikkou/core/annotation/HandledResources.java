package io.streamthoughts.jikkou.core.annotation;

import static java.lang.annotation.ElementType.TYPE;

import java.lang.annotation.Documented;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Defines a set of resources type that can be handled by an extension.
 * An empty set implies that the extension can handle any resource-type or takes care of checking resource-type itself.
 */
@Documented
@Retention(RetentionPolicy.RUNTIME)
@Target({TYPE})
public @interface HandledResources {
    HandledResource[] value() default {};
}
